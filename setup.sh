#!/bin/bash

# Mac Audio Streamer Setup Script
# This script automatically configures the project for your local network

echo "ðŸŽµ Mac Audio Streamer Setup"
echo "=========================="
echo ""

# Check if Node.js is installed
if ! command -v node &> /dev/null; then
    echo "âŒ Node.js is not installed. Please install Node.js first:"
    echo "   Visit: https://nodejs.org/"
    exit 1
fi

# Check if FFmpeg is installed
if ! command -v ffmpeg &> /dev/null; then
    echo "âŒ FFmpeg is not installed. Please install FFmpeg first:"
    echo "   Run: brew install ffmpeg"
    exit 1
fi

echo "âœ… Node.js and FFmpeg are installed"
echo ""

# Get local IP address
echo "ðŸ” Detecting your local IP address..."
LOCAL_IP=$(ifconfig | grep "inet " | grep -v 127.0.0.1 | head -1 | awk '{print $2}')

if [ -z "$LOCAL_IP" ]; then
    echo "âŒ Could not detect your local IP address"
    echo "   Please make sure you're connected to WiFi"
    exit 1
fi

echo "âœ… Found your local IP: $LOCAL_IP"
echo ""

# Install dependencies
echo "ðŸ“¦ Installing Node.js dependencies..."
npm install

if [ $? -ne 0 ]; then
    echo "âŒ Failed to install dependencies"
    exit 1
fi

echo "âœ… Dependencies installed"
echo ""

# Update the QR code with their IP
echo "ðŸ”§ Updating QR code with your IP address..."
sed -i '' "s/192\.168\.0\.101/$LOCAL_IP/g" index.html

if [ $? -ne 0 ]; then
    echo "âŒ Failed to update IP address in index.html"
    exit 1
fi

echo "âœ… QR code updated with IP: $LOCAL_IP"
echo ""

# Create a simple start script
echo "ðŸ“ Creating start script..."
cat > start-server.sh << EOF
#!/bin/bash
echo "ðŸŽµ Starting Mac Audio Streamer..."
echo "ðŸ“± Your iPhone can connect to: $LOCAL_IP:3001"
echo "ðŸŒ QR code available at: http://localhost:3000"
echo ""
echo "Press Ctrl+C to stop the server"
echo ""

# Start both servers
node udp-server.js &
UDP_PID=\$!

node qr-server.js &
QR_PID=\$!

# Function to cleanup on exit
cleanup() {
    echo ""
    echo "ðŸ›‘ Stopping servers..."
    kill \$UDP_PID \$QR_PID 2>/dev/null
    exit 0
}

# Trap Ctrl+C
trap cleanup SIGINT

# Wait for processes
wait
EOF

chmod +x start-server.sh

echo "âœ… Start script created"
echo ""

# Create README for users
echo "ðŸ“– Creating setup instructions..."
cat > SETUP_INSTRUCTIONS.md << EOF
